				Формат Электронного письма:

				Заголовок1\r\n
				...\r\n
				ЗаголовокN\r\n
				...\r\n
				Тело\r\n

				При этом Заглоовок - строка (ИмяЗаголовка:Значение):
					From          - Адрес отрправителя
					To   					- Адрес Получателя
					Subject    		- Тема
					Reply-to 			-	Формирует письмо для адресата указанного тут, если нет, то по умолчанию: From 
					Content-type 	-	MIME тип

				!!! Значение заголовка может состоять из нескольких строк, при этом следующая строка должна начинаться с пробела/таба
				Тело письма может быть любым, в случае текста ичего преобр не надо,
				В случае HTML надо кодироать base64_encode()


				Отправка письма:
					
					bool mail(string $to, string $subject, string $msg [,string $headers]) 
					Пример:
						mail( 
									"somebody@mail.ru", #To 
									"Mail robot", # Subject 
									"Hello!\nThis is autogenerated message - please do not reply." 
								); 
						$headers - параметр дополнительных заголовов, вставляется между первыми -основными заголовками и телом
						Пример:
						join("\r\n", array("From: webmaster@$SERVER_NAME", "Reply-To: webrnaster@$SERVER_NAME", "X-Mailer: PHP/".phpversion()))

				Письма по шаблонам (Обычно так)
